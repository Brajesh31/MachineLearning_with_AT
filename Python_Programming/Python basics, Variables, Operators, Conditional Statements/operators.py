# operators.py

# Arithmetic Operators
a = 10
b = 3

addition = a + b
subtraction = a - b
multiplication = a * b
division = a / b
modulus = a % b
exponent = a ** b

print(f"Addition: {addition}")
print(f"Subtraction: {subtraction}")
print(f"Multiplication: {multiplication}")
print(f"Division: {division}")
print(f"Modulus: {modulus}")
print(f"Exponent: {exponent}")

# Comparison Operators
greater_than = a > b
less_than = a < b
equal_to = a == b

print(f"Is a greater than b?: {greater_than}")
print(f"Is a less than b?: {less_than}")
print(f"Is a equal to b?: {equal_to}")

# Logical Operators
x = True
y = False

print(f"x AND y: {x and y}")
print(f"x OR y: {x or y}")
print(f"NOT x: {not x}")
